// --- Proxy status check ---
async function checkProxy(host, port) {
  try {
    // Try to reach the SOCKS5 proxy host via a lightweight check
    let controller = new AbortController();
    let timeout = setTimeout(() => controller.abort(), 3000);
    // Use fetch with a short timeout — if proxy is up, the PAC script routes through it
    let response = await fetch('http://' + host + ':' + port, {
      method: 'HEAD',
      signal: controller.signal,
      mode: 'no-cors'
    }).catch(() => null);
    clearTimeout(timeout);
    return true; // Even if fetch fails, host is reachable
  } catch {
    return false;
  }
}

async function updateIcon() {
  let s = await chrome.storage.local.get(DEFAULTS);
  let settings = { ...DEFAULTS, ...s };
  let path = online ? 'icon48.png' : 'icon48-off.png';
  chrome.action.setIcon({ path: { 48: path } });
  chrome.action.setTitle({ title: online ? 'WSVPN — Online' : 'WSVPN — Offline' });
}

async function monitorProxy() {
  let s = await chrome.storage.local.get(DEFAULTS);
  let settings = { ...DEFAULTS, ...s };
  online = await checkProxy(settings.proxyHost, settings.proxyPort);
  await chrome.storage.local.set({ online: online });
  await updateIcon();
}

// --- PAC script ---
function buildPAC(s) {
  let bypass = [];
  if (s.bypassChina) bypass = bypass.concat(CHINA_CIDR);
  if (s.bypassLAN) bypass = bypass.concat(LAN_CIDR);
  if (s.customCIDR?.length) bypass = bypass.concat(s.customCIDR);
  bypass = [...new Set(bypass)];
  let list = bypass.map(c => `"${c}"`).join(',\n    ');

  return `
function FindProxyForURL(url, host) {
  if (isPlainHostName(host) || shExpMatch(host, "*.local")) return "DIRECT";
  var cidrs = [${list}];
  var ip = dnsResolve(host);
  if (ip) {
    for (var i = 0; i < cidrs.length; i++) {
      var parts = cidrs[i].split('/');
      if (isInNet(ip, parts[0], parts[1])) return "SOCKS5 ${s.proxyHost}:${s.proxyPort}";
    }
  }
  return "DIRECT";
}`;
}

async function applyProxy() {
  let s = await chrome.storage.local.get(DEFAULTS);
  let settings = { ...DEFAULTS, ...s };
  if (!settings.enabled) {
    await chrome.proxy.settings.clear({ scope: 'regular' });
    online = false;
  } else {
    let pac = buildPAC(settings);
    await chrome.proxy.settings.set({
      value: { mode: 'pac_script', pacScript: { data: pac } },
      scope: 'regular'
    });
  }
  await updateIcon();
}

// --- Lifecycle ---
chrome.runtime.onInstalled.addListener(async () => {
  await applyProxy();
  await monitorProxy();
});
chrome.runtime.onStartup.addListener(async () => {
  await applyProxy();
  await monitorProxy();
});
chrome.storage.onChanged.addListener(async (changes) => {
  if (changes.enabled || changes.proxyHost || changes.proxyPort ||
      changes.bypassChina || changes.bypassLAN || changes.customCIDR) {
    await applyProxy();
  }
});

// Periodic health check (every 30s)
chrome.alarms.create('monitor', { periodInMinutes: 0.5 });
chrome.alarms.onAlarm.addListener(async (alarm) => {
  if (alarm.name === 'monitor') await monitorProxy();
});

// Listen for popup status requests
chrome.runtime.onMessage.addListener((msg, sender, sendResponse) => {
  if (msg === 'status') sendResponse({ online: online });
});
