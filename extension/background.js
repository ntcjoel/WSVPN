// Load settings and apply proxy configuration
async function loadAndApply() {
  const settings = await chrome.storage.local.get(DEFAULT_SETTINGS);
  const s = { ...DEFAULT_SETTINGS, ...settings };

  if (!s.enabled) {
    await chrome.proxy.settings.clear({ scope: 'regular' });
    return;
  }

  // Build PAC script for smart routing
  const pacScript = buildPAC(s);
  await chrome.proxy.settings.set({
    value: { mode: 'pac_script', pacScript: { data: pacScript } },
    scope: 'regular'
  });
}

function buildPAC(s) {
  let bypassCIDRs = [];
  if (s.bypassChina) bypassCIDRs = bypassCIDRs.concat(CHINA_CIDR);
  if (s.bypassLAN) bypassCIDRs = bypassCIDRs.concat(LAN_CIDR);
  if (s.customCIDR && s.customCIDR.length) bypassCIDRs = bypassCIDRs.concat(s.customCIDR);

  // Remove duplicates
  bypassCIDRs = [...new Set(bypassCIDRs)];

  const cidrList = bypassCIDRs.map(c => `"${c}"`).join(',\n    ');

  return `
function FindProxyForURL(url, host) {
  // Direct connection for LAN addresses
  if (isPlainHostName(host)) return "DIRECT";
  if (shExpMatch(host, "*.local")) return "DIRECT";

  // Check bypass CIDR ranges — route through VPN proxy
  var bypassCIDRs = [
    ${cidrList}
  ];

  // Convert host to IP for CIDR matching
  var resolvedIP = dnsResolve(host);
  if (resolvedIP) {
    for (var i = 0; i < bypassCIDRs.length; i++) {
      if (isInNet(resolvedIP, bypassCIDRs[i].split('/')[0], bypassCIDRs[i].split('/')[1])) {
        return "SOCKS5 ${s.proxyHost}:${s.proxyPort}";
      }
    }
  }

  // Default: direct connection (or proxy all if mode is 'proxyAll')
  ${s.mode === 'bypass' ? 'return "DIRECT";' : 'return "SOCKS5 ' + s.proxyHost + ':' + s.proxyPort + '";'}
}`;
}

// Initialize and listen for changes
chrome.runtime.onInstalled.addListener(loadAndApply);
chrome.runtime.onStartup.addListener(loadAndApply);
chrome.storage.onChanged.addListener((changes) => {
  if (changes.enabled || changes.mode || changes.proxyHost || changes.proxyPort ||
      changes.bypassChina || changes.bypassLAN || changes.customCIDR) {
    loadAndApply();
  }
});

